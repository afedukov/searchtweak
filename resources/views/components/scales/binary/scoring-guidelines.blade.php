This scale is the simplest, representing whether the result is relevant or not. It uses two grades: **Irrelevant** and **Relevant**.

---

# Scoring Guidelines
- **Irrelevant**
  - The result does not match the search intent or query at all.
  - *Example*: Searching for "laptop" and getting a chair as a result.

- **Relevant**
  - The result directly aligns with the search intent or query.
  - *Example*: Searching for "laptop" and getting a MacBook as a result.
